""" Slicing is a powerful technique that allows 
you to precisely select data from a sequence.

In this lesson, you'll learn some new slicing techniques. """

cart = ['lamp', 'candles', 'chair', 'carpet']
print(cart[:2]) #so we have removed the first number 

#sort of clever before you code you click the words/icons

